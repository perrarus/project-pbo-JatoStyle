    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
                                         
        String email = emailInput.getText().trim();
        String password = new String(passwordInput.getPassword());

        AuthService authService = new AuthService();

        if (email.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                "Email dan password harus diisi!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // validasi format email
        if (!authService.isValidEmail(email)) {
            JOptionPane.showMessageDialog(this,
                "Format email tidak valid!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            // coba login sbg resto
            Restoran resto = authService.loginRestoran(email, password);
            if (resto != null) {
                JOptionPane.showMessageDialog(this, "Login Berhasil! (Restoran)");
                new DashboardRestoranFrame(resto).setVisible(true);
                this.dispose();
                return;
            }
            
            // coba login sbg admin
            Admin admin = authService.loginAdmin(email, password);
            if (admin != null) {
                JOptionPane.showMessageDialog(this, "Login Berhasil! (Admin)");
                new DashboardAdminFrame(admin).setVisible(true);
                this.dispose();
                return;
            }

            // coba login sbg user
            User user = authService.loginUser(email, password);
            if (user != null) {
                JOptionPane.showMessageDialog(this, "Login Berhasil! (User)");
                DashboardUserFrame d = new DashboardUserFrame();
                d.setUserData(user);
                d.setVisible(true);
                this.dispose();
                return;
            }

            JOptionPane.showMessageDialog(this,
                "Email atau password salah!", "Error", JOptionPane.ERROR_MESSAGE);

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,
                "Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }   
    }//GEN-LAST:event_loginButtonActionPerformed

