    private boolean isValidNoTelp(String noTelp) {
        if (noTelp == null || noTelp.trim().isEmpty()) {
            return false;
        }
        // hanya angka, min 10 digit, maks 13 digit
        return noTelp.matches("\\d{10,13}");
    }

    private boolean isValidPassword(String password) {
        if (password == null || password.trim().isEmpty()) {
            return false;
        }
        // minimal 8 karakter
        return password.length() >= 8;
    }
    
