    private void registerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerButtonActionPerformed
        String nama = namaInput.getText().trim();
        String email = emailInput.getText().trim();
        String noTelp = noTelpInput.getText().trim();
        String password = new String(passwordInput.getPassword());
        String confirmPassword = new String(confirmPasswordInput.getPassword());

        // object AuthService
        AuthService authService = new AuthService();

        // validasi input kosong
        if (nama.isEmpty() || email.isEmpty() || noTelp.isEmpty() || password.isEmpty() || confirmPassword.isEmpty()) {
            JOptionPane.showMessageDialog(this, 
                "Semua field harus diisi!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // validasi format email
        if (!authService.isValidEmail(email)) {
            JOptionPane.showMessageDialog(this, 
                "Format email tidak valid!\n" +
                "• Harus mengandung @\n" +
                "• Harus mengandung domain (.com, .net, dll)\n" +
                "• Minimal 5 karakter", 
                "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // validasi no telp, min 10 max 13 digit
        if (!isValidNoTelp(noTelp)) {
            JOptionPane.showMessageDialog(this, 
                "Format nomor telepon tidak valid!\n" +
                "• Harus berupa angka\n" +
                "• Minimal 10 digit\n" +
                "• Maksimal 13 digit", 
                "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // validasi password min 8 karakter)
        if (!isValidPassword(password)) {
            JOptionPane.showMessageDialog(this, 
                "Password terlalu pendek!\n" +
                "• Minimal 8 karakter", 
                "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // validasi confirm password
        if (!password.equals(confirmPassword)) {
            JOptionPane.showMessageDialog(this, 
                "Password dan Confirm Password tidak sama!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
        }

        // validasi email sudah terdaftar
        if (authService.isEmailExists(email, "user")) {
            JOptionPane.showMessageDialog(this, 
                "Email sudah terdaftar!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // proses register
        try {

            // buat user dengan password yang sudah dihash
            User newUser = new User(nama, email, password, noTelp);

            boolean success = authService.registerUser(newUser);

            if (success) {
                JOptionPane.showMessageDialog(this, 
                    "Registrasi berhasil! Silakan login.", "Success", JOptionPane.INFORMATION_MESSAGE);
                new LoginFrame().setVisible(true);
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, 
                    "Registrasi gagal!", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, 
                "Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_registerButtonActionPerformed

